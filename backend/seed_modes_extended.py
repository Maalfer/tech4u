"""
seed_modes_extended.py
Añade ~100 ejercicios nuevos a cada uno de los 4 modos interactivos:
  fill_blank | find_bug | order_clauses | reverse_query

Distribución por dataset:
  Tienda      → 40 por modo
  Empresa IT  → 30 por modo
  Pokemon     → 30 por modo
  TOTAL       → 100 nuevos por modo

Uso: python seed_modes_extended.py
"""
import json, sys, os
sys.path.insert(0, os.path.dirname(__file__))

from sqlalchemy import func
from database import SessionLocal, SQLDataset, SQLExercise, create_tables
from routers.sql_skills import _compute_expected

db = SessionLocal()


def add_exercises(dataset_name: str, mode: str, exercises_data: list):
    """Inserta ejercicios evitando duplicados por título."""
    ds = db.query(SQLDataset).filter(SQLDataset.name == dataset_name).first()
    if not ds:
        print(f"  ⚠  Dataset '{dataset_name}' no encontrado — saltando")
        return

    # Orden máximo actual para este tipo en este dataset
    max_order = db.query(func.max(SQLExercise.order_num)).filter(
        SQLExercise.dataset_id == ds.id,
        SQLExercise.exercise_type == mode,
    ).scalar() or 0

    added = updated = errors = 0
    for i, data in enumerate(exercises_data):
        try:
            expected = _compute_expected(ds.schema_sql, ds.seed_sql, data["solution_sql"])
        except Exception as e:
            print(f"    ✗ SQL inválido en '{data['title']}': {e}")
            errors += 1
            continue

        existing = db.query(SQLExercise).filter(
            SQLExercise.dataset_id == ds.id,
            SQLExercise.exercise_type == mode,
            SQLExercise.title == data["title"],
        ).first()

        fields = dict(
            category=data.get("category", mode.replace("_", " ").title()),
            difficulty=data.get("difficulty", "intermedio"),
            description=data.get("description", data["title"]),
            wiki_title=data.get("wiki_title"),
            wiki_content=data.get("wiki_content"),
            wiki_syntax=data.get("wiki_syntax"),
            wiki_example=data.get("wiki_example"),
            solution_sql=data["solution_sql"],
            expected_result=expected,
            xp_reward=data.get("xp_reward", 50),
            exercise_type=mode,
            template_sql=data.get("template_sql"),
            buggy_sql=data.get("buggy_sql"),
            fragments=data.get("fragments"),
            is_active=True,
        )

        if existing:
            for k, v in fields.items():
                setattr(existing, k, v)
            updated += 1
        else:
            max_order += 1
            db.add(SQLExercise(
                dataset_id=ds.id,
                title=data["title"],
                order_num=max_order,
                **fields,
            ))
            added += 1

    db.commit()
    print(f"  [{dataset_name}] +{added} creados, {updated} actualizados, {errors} errores")


